<!-- 150を足しても、150を引いても、平方数になる自然数を求めよ -->

<?php

$num_array = array();
for($i=0; $i<100000; $i++) {
  $sqrt = floor(sqrt($i));
    if($sqrt*$sqrt == $i) {
      $num_array[] = $i;
    }
}

for($n=0; $n<100000; $n++){
  $is_a = in_array($n + 150, $num_array);
  $is_b = in_array($n - 150, $num_array);

  if($is_a && $is_b) {
    var_dump($n);
  }
}
